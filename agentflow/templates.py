def _skeleton(
    title: str,
    writes: str,
    not_writes: str,
    sections: list[tuple[str, str]],
) -> str:
    lines = [
        f"# {title}",
        "",
        "<!-- ═══════════════════════════════════════════════════════════",
        "     本文件边界（不可删除，不可改写）",
        f"     只写：{writes}",
        f"     不写：{not_writes}",
        "     判断标准：如果一段内容换到别的项目还能用，它就不属于这里。",
        "     ═══════════════════════════════════════════════════════════ -->",
        "",
        "<!-- 维护契约：由首个接触本项目的 AI 编码助手分析代码后填写；",
        "     后续每次接手时核对，与代码不符就修正。不要删除章节标题。 -->",
        "",
    ]
    for heading, hint in sections:
        lines.extend(
            [
                f"## {heading}",
                "",
                f"<!-- {hint} -->",
                "",
            ]
        )
    return "\n".join(lines)


