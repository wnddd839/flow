def agents_md() -> str:
    return dedent(
        """\
        # AGENTS.md

        本文件是本项目的**唯一总入口**。开工前必读，每次必读。

        ## Flow 与本项目的关系

        Flow 是轻巧的离线 CLI，只负责铺设规范地基：生成 `.agentflow/` 骨架、
        按需写入所选 AI 工具的薄入口、`check` 校验文件是否齐全。

        Flow **不**绑定特定 agent 或编辑器，**不**编排工作流，**不**强制模型行为。
        文档填写与维护靠本文件中的契约与提示词；是否遵守取决于 AI 与开发者，而非工具拦截。

        ## 项目文档（位于 `.agentflow/`）

        规范分散在以下文件中，**每个文件边界严格不重叠**。
        需要时按索引阅读，不要把所有信息都塞进本文件：

        - [`project.md`](project.md) — 项目是什么：定位、技术栈、架构、运行方式
        - [`conventions.md`](conventions.md) — 怎么写代码：命名、结构、风格、禁用模式
        - [`business.md`](business.md) — 业务是什么：领域概念、核心流程、术语表
        - [`pitfalls.md`](pitfalls.md) — 踩过的坑：历史教训、不再重蹈的决策
        - [`skills/README.md`](skills/README.md) — 专项 skill 路由表

        ## 工作纪律

        1. **开工前**：通读 `.agentflow/` 下所有 `.md`。重述目标、范围、非目标后再动手。
        2. **范围最小**：只改与当前请求相关的文件。不碰无关模块。
        3. **验证优先**：改完先跑可用的检查（类型/测试/lint），再报告完成。
        4. **交接标准**：完成时报告——改了哪些文件、跑了什么命令、结果、遗留风险、下一步。

        ## 文档维护契约

        以下约定面向 AI 编码助手，靠提示词生效，Flow 本身不强制执行。

        - **首次接手**：若 `project.md` 等仍有未填章节，**第一项任务**是分析代码库并填写，
          然后再做用户要求的事。按每个文件顶部的边界声明填写。
        - **改动后更新**：完成有意义的改动后，更新对应文档。
          改了架构 → `project.md`；定了新规范 → `conventions.md`；
          理解了新业务 → `business.md`；踩了坑 → `pitfalls.md`。
        - **不重复**：每条信息只出现在一个文档里。写到一半发现「别处也有」，
          说明放错位置——按边界声明判断归属，只保留一处。
        - **宁缺毋滥**：某个文档可以很短，但不要为「完整」而堆砌。空章节比错位章节好。
        - **以代码为准**：文档与代码冲突时，以代码为准，当场修正文档。
        - **过时即删**：过时的文档比没有文档更糟——宁可删，不要留错的。

        ## 何谓「大改动」（需要同步文档）

        满足任一条即属于必须更新文档的大改动：

        - 新增/删除/重命名 模块、命令、对外行为或目录结构 → `project.md`、`business.md`
        - 确立或修改 编码约定、禁用模式 → `conventions.md`
        - 引入新领域概念、改变核心流程或术语 → `business.md`
        - 踩坑、否决方案、识别敏感区 → `pitfalls.md`

        仅修 typo、调措辞、补单测断言等不改变行为的小改，可不动文档。

        ## 完成定义（收尾自检，大改动不得跳过）

        声称「已完成 / 可提交 / 可 merge」之前，逐条确认：

        1. 代码改动已通过可用检查（测试 / lint / 编译）。
        2. 若属于上文「大改动」，已更新对应 `.agentflow/` 文档。
        3. 文档与代码一致；每条信息只出现在一个文档里。
        4. 已按交接标准报告改动文件、命令、结果、风险。

        **未完成同步前，不要先宣布完成。**

        ## Skill 路由

        涉及专项操作时，先查 [`skills/README.md`](skills/README.md)
        的路由表，匹配则先读对应 skill 再动手。
        """
    )


